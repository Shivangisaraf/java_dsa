package DSA_JAVA;

import java.util.ArrayList;

public class stringConcatenateOperator {
    public static void main(String[] args) {
        System.out.println('a'+'b');//ASCII VALUE->>195
        System.out.println("a"+"b");//ab
        System.out.println('a'+3);//97+3
        System.out.println((char)('a'+3));//changing to character
        System.out.println("name"+new ArrayList<>());//name[]

    }
}
